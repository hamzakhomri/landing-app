                    submenus: [
                        { title: 'Dress & Frock' },
                        { title: 'Earrings' },
                        { title: 'Necklace' },
                        { title: 'Makeup Kit' }
                    ]
                    },
                ],
        }
    },
    
  methods: {
    closeMobileMenu(index) {
      this.mobileMenus[index].isActive = false;
      this.isOverlayActive = false;
    },
    openMobileMenu(index) {
      this.mobileMenus[index].isActive = true;
      this.isOverlayActive = true;
    },



    closeMobileMenus() {
      this.mobileMenus.forEach(menu => (menu.isActive = false));
      this.isOverlayActive = false;
    },
    toggleAccordion(index) {
      this.accordionItems.forEach((item, i) => {
        if (i !== index) {
          item.isActive = false;
        }
      });
      this.accordionItems[index].isActive = !this.accordionItems[index].isActive;
    },
  },
    
}
